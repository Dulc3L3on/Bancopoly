    public void cargarJugadores(){
        /*if(cbBx_jugadores.getItemCount()>0){
            cbBx_jugadores.removeAllItems();
        }*/
        
        for (int numeroJUgadores = 0; numeroJUgadores < DesarrolloPartida.jugadores.length; numeroJUgadores++) {
            cbBx_jugadores.addItem(DesarrolloPartida.jugadores[numeroJUgadores].obtnerNombre());
        }
    }
    
    public void mostrarINformaciónCorrespondiente(int numeroJugador){
        
        if(DesarrolloPartida.jugadores[numeroJugador].obtenerCondena()>0){
            String condena=String.valueOf(DesarrolloPartida.jugadores[numeroJugador].obtenerCondena());
            lbl_condena.setText("Turnos de condena -> "+ condena);
        }else{
            lbl_condena.setText("Libre como el viento");            
        }        
        if(DesarrolloPartida.jugadores[numeroJugador].obtenerTurnosPerdidos()>0){
            String perdidos= String.valueOf(DesarrolloPartida.jugadores[numeroJugador].obtenerTurnosPerdidos());
            lbl_turnosPerdidos.setText("Turnos perdidos -> "+ perdidos);
        }else{
            lbl_turnosPerdidos.setText("Sin turnos perdidos");
        }
        
    }
    

