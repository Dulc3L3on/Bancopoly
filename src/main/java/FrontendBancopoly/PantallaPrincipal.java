    private void btn_AceptarVerRankingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AceptarVerRankingActionPerformed
        if(archivo.verificarArchivo(Ranking.darPath(darTipoListado()))){            
            
                  tbl_Ranking.setModel(new javax.swing.table.DefaultTableModel(
            Ranking.crearListadoDatos(formaOrden(), darTipoListado(), darTipoVisualizacion()),
            new String [] {
                "Nombre", "Riquezas", "Estado"
            }
        ) {
            Class[] types = new Class [] {
                java.lang.String.class, java.lang.Integer.class, java.lang.String.class
            };
            boolean[] canEdit = new boolean [] {
                false, false, false
            };

            public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit [columnIndex];
            }
        });
            
        }else{
            JOptionPane.showMessageDialog(null, "Por el momento no hay ranking que mostrar", "No existe registro", JOptionPane.WARNING_MESSAGE);
        }
        
        tbl_Ranking.updateUI();
    }//GEN-LAST:event_btn_AceptarVerRankingActionPerformed

