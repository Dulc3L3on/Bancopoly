    private void rBtn_SalirYGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rBtn_SalirYGuardarActionPerformed
        
        ManejadorDeArchivos manejador = new ManejadorDeArchivos();
        
        reestablecerJuego();
        
        manejador.escribirArchivo();  
        for (int tipoArchivo = 1; tipoArchivo < 3; tipoArchivo++) {
            Ranking.anadirAlRanking(tipoArchivo);
        }        
        Tablero.controlarVentana();
        this.dispose();
    }//GEN-LAST:event_rBtn_SalirYGuardarActionPerformed

