    public void reestablecerJuego(){
        if(Integer.parseInt(Tablero.labelesTiempo[0].getText())==0 && Integer.parseInt(Tablero.labelesTiempo[1].getText())==0 && Integer.parseInt(Tablero.labelesTiempo[2].getText())==0){
            SolicitudDatos.registroDatos.reestablecerTiempo();
            //aquí se reestablece el juego... que sería con el mismo método para sacar a un jugador por quiebra... solo que esta vez a todos los jugaores,
            //recuerda que no habrá problema con emplear ese método, para lo jugaores pues registro está haciendo referencia a su contenido, así que cha chan tendrá
            //las modificaciones que se hagan a lo largo del desarrollo, lo mismo para el recorrido lógico, puesto que se le hizo una vez referencia entonces 
            //obtendrá los cb respectivos... de lo demás no hay pena puesto que tiene los datos que inicialmente se le asignaron por lo tanto está todo bien... ya que
            //la única manera para que cb sería en la edición, lo cual ya está contemplado en su respectivo espacio
        }
    }


